import mongoose from "mongoose";

const messageSchema = new mongoose.Schema({
    chatRequest: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'ChatRequest',
        required: true
    },
    sender: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    content: {
        type: String,
        required: true
    },
    read: {
        type: Boolean,
        default: false
    }
}, {
    timestamps: true
});

const Message = mongoose.model('Message', messageSchema);

export default Message;
